package algorithms.imageProcessing;

/**
 *
 * @author nichole
 */
public class PairInt {
    
    private int x;
    private int y;
    
    public PairInt() {
    }
    public PairInt(int xPoint, int yPoint) {
        x = xPoint;
        y = yPoint;
    }
    public void setX(int xPoint) {
        x = xPoint;
    }
    public void setY(int yPoint) {
        y = yPoint;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
}
