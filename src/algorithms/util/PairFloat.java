package algorithms.util;

/**
 *
 * @author nichole
 */
public class PairFloat {
    
    private float x;
    private float y;
    
    public PairFloat() {
    }
    public PairFloat(float xPoint, float yPoint) {
        x = xPoint;
        y = yPoint;
    }
    public void setX(float xPoint) {
        x = xPoint;
    }
    public void setY(float yPoint) {
        y = yPoint;
    }
    public float getX() {
        return x;
    }
    public float getY() {
        return y;
    }
}
